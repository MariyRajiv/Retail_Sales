import React from 'react';

export default function Pagination({ page, pageSize, total, onChange }) {
  const totalPages = Math.ceil(total / pageSize) || 1;
  return (
    <div className="flex items-center gap-2 justify-center my-4">
      <button disabled={page<=1} onClick={()=>onChange(page-1)} className="px-3 py-1 border rounded disabled:opacity-40">Prev</button>
      <div>Page {page} of {totalPages}</div>
      <button disabled={page>=totalPages} onClick={()=>onChange(page+1)} className="px-3 py-1 border rounded disabled:opacity-40">Next</button>
    </div>
  );
}
